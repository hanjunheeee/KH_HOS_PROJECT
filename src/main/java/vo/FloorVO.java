package vo;

public class FloorVO {
	
	private int floor_idx, floor_info;
	private String floor_name;
	public int getFloor_idx() {
		return floor_idx;
	}
	public void setFloor_idx(int floor_idx) {
		this.floor_idx = floor_idx;
	}
	public int getFloor_info() {
		return floor_info;
	}
	public void setFloor_info(int floor_info) {
		this.floor_info = floor_info;
	}
	public String getFloor_name() {
		return floor_name;
	}
	public void setFloor_name(String floor_name) {
		this.floor_name = floor_name;
	}
	
	
	
}
